import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import "https://deno.land/x/xhr@0.1.0/mod.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const OPENAI_API_KEY = Deno.env.get('OPENAI_API_KEY');
    if (!OPENAI_API_KEY) {
      throw new Error('OPENAI_API_KEY is not set');
    }

    const { model = "LPT-4" } = await req.json();

    // System instruction for Deta voice
    const systemInstruction = `You are Deta AI, an advanced voice assistant developed by LiskCell.

CORE IDENTITY:
- Your name is Deta
- You were trained and developed by LiskCell
- Your current model is ${model} (Gemini 3 class performance)

VOICE BEHAVIOR:
- Speak naturally and conversationally
- Keep responses concise for voice - aim for 1-3 sentences unless user asks for detail
- Be friendly, helpful, and engaging
- Respond in the same language the user speaks

CAPABILITIES:
- Natural conversation and Q&A
- Creative assistance
- Coding help
- General knowledge

RESTRICTIONS:
- Never mention OpenAI, GPT, or other AI companies as your creator
- Always say you are Deta, trained by LiskCell
- If asked about your model, say you are ${model}`;

    // Request an ephemeral token from OpenAI
    const response = await fetch("https://api.openai.com/v1/realtime/sessions", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${OPENAI_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-4o-realtime-preview-2024-12-17",
        voice: "alloy",
        instructions: systemInstruction,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("OpenAI API error:", response.status, errorText);
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    const data = await response.json();
    console.log("Voice session created successfully");

    return new Response(JSON.stringify(data), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  } catch (error) {
    console.error("Error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
